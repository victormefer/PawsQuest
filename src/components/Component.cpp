#include "Component.h"
#include "..\entities\Entity.h"

/*************************************************************
 *
 * Construtor
 *
 *************************************************************/
Component::Component()
{
	isEnable = true;
	owner = NULL;
}

/*************************************************************
 *
 * Destrutor
 *
 *************************************************************/
Component::~Component()
{

}

/*************************************************************
 *
 * Verifica se o componente esta enable ou disable
 *
 *************************************************************/
bool Component::IsEnable()
{
	return isEnable;
}

/*************************************************************
 *
 * GetName
 *
 *************************************************************/
std::string Component::GetName()
{
	return name;
}

/*************************************************************
 *
 * SetName
 *
 *************************************************************/
void Component::SetName(std::string name)
{
	this->name = name;
}

/*************************************************************
 *
 * Retorna a entidade dona do componente
 *
 *************************************************************/
Entity* Component::GetOwner()
{
	return owner;
}

/*************************************************************
 *
 * SetOwner
 *
 *************************************************************/
void Component::SetOwner(Entity* entity)
{
	this->owner = entity;
}

/*************************************************************
 *
 * Enable
 *
 *************************************************************/
void Component::Enable()
{
	this->isEnable = true;
}

/*************************************************************
 *
 * Disable
 *
 *************************************************************/
void Component::Disable()
{
	this->isEnable = false;
}
