#include "Music.h"

/*************************************************************
 *
 * Construtor
 *
 *************************************************************/
Music::Music()
{
	music = NULL;
}

/*************************************************************
 *
 * Construtor
 *
 *************************************************************/
Music::Music(const char* file)
{
	Open(file);
}

/*************************************************************
 *
 * Abrir musica
 *
 *************************************************************/
void Music::Open(const char* file)
{
	music = ResourceManager::GetMusic(file);
}

/*************************************************************
 *
 * Tocar musica
 *
 *************************************************************/
void Music::Play(int times)
{
	Mix_PlayMusic(music.get(), times);
}

/*************************************************************
 *
 * Parar musica
 *
 *************************************************************/
void Music::Stop()
{
	Mix_FadeOutMusic(1000);
}

/*************************************************************
 *
 * Musica esta aberta
 *
 *************************************************************/
bool Music::IsOpen()
{
	return music == NULL ? false : true;
}

/*************************************************************
 *
 * IsPlaying
 *
 *************************************************************/
bool Music::IsPlaying()
{
	return Mix_PlayingMusic();
}
